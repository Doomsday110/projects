def is_video_valid(video_path):
    """Checks if a video file is valid and not empty."""
    cap = cv2.VideoCapture(video_path)
    valid = cap.isOpened() and int(cap.get(cv2.CAP_PROP_FRAME_COUNT)) > 0
    cap.release()
    return valid

#FUNCTION: Flip video (horizontal or vertical)
def flip_video(video_path, output_path, mode):
    """Flips a video (horizontal or vertical) and saves it."""
    cap = cv2.VideoCapture(video_path)
    fourcc = cv2.VideoWriter_fourcc(*'mp4v')
    frame_width, frame_height = int(cap.get(3)), int(cap.get(4))
    fps = int(cap.get(cv2.CAP_PROP_FPS))

    if frame_width == 0 or frame_height == 0 or fps == 0:
        return False  # Skip invalid video

    out = cv2.VideoWriter(output_path, fourcc, fps, (frame_width, frame_height))

    while cap.isOpened():
        ret, frame = cap.read()
        if not ret:
            break
        frame = cv2.flip(frame, mode)  # Flip mode: 1 (Horizontal), 0 (Vertical)
        out.write(frame)

    cap.release()
    out.release()

    return is_video_valid(output_path)


