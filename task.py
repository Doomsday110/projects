def compute_optical_flow(video_path):
    """Computes optical flow to estimate movement (x, y) for each frame."""
    cap = cv2.VideoCapture(video_path)
    ret, prev_frame = cap.read()

    if not ret:
        raise ValueError(f"Could not read video: {video_path}")

    prev_gray = cv2.cvtColor(prev_frame, cv2.COLOR_BGR2GRAY)
    path = []

    while cap.isOpened():
        ret, frame = cap.read()
        if not ret:
            break

        gray = cv2.cvtColor(frame, cv2.COLOR_BGR2GRAY)
        flow = cv2.calcOpticalFlowFarneback(prev_gray, gray, None, 0.5, 3, 15, 3, 5, 1.2, 0)
        dx, dy = np.mean(flow[..., 0]), np.mean(flow[..., 1])
        path.append([dx, dy])
        prev_gray = gray

    cap.release()
    return np.array(path) if path else np.zeros((1, 2))

def save_optical_flow(video_folder, save_path):
    """Computes and saves optical flow for all videos in a given folder."""
    video_files = sorted([os.path.join(video_folder, f) for f in os.listdir(video_folder) if f.endswith(".mp4")])
    flow_data = {}

    print(f"\nProcessing videos in '{video_folder}' ({len(video_files)} found)...")

    for video_path in tqdm(video_files, desc=f"Processing {video_folder}", unit="video"):
        path = compute_optical_flow(video_path)
        flow_data[video_path] = path
        tqdm.write(f"✔ Processed {os.path.basename(video_path)} | Optical Flow Shape: {path.shape}")

    with open(save_path, "wb") as f:
        pickle.dump(flow_data, f)

    print(f"Optical flow saved to {save_path}")

