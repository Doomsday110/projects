def test(model, test_loader):
    model.eval()
    best_video = None
    best_score = float("inf")

    with torch.no_grad():
        for frames, _, video_paths in test_loader:
            frames = frames.to(device)
            output = model(frames).cpu().numpy()

            for i, video_name in enumerate(video_paths):
                frame_outputs = output[i]  # Model predictions per frame

                # Penalize SHARP turns (sudden direction changes)
                sharp_turn_penalty = np.sum(np.abs(np.diff(frame_outputs, n=2)))

                # Allow smooth diagonal motion (by using first-order differences)
                smooth_motion_score = np.sum(np.abs(np.diff(frame_outputs)))

                # Slight penalty for longer videos
                time_penalty = len(frame_outputs) * 0.05

                # Final Score: Balance between straight and smooth diagonal motion
                score = sharp_turn_penalty * 1.5 + smooth_motion_score * 0.5 + time_penalty

                if score < best_score:
                    best_score = score
                    best_video = video_name

    print("\nBest Path Video:", best_video)
test(model, test_loader)