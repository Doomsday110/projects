def train(model, train_loader, eval_loader, epochs=300, model_dir="model/"):
    os.makedirs(model_dir, exist_ok=True)

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    model.to(device)

    criterion = nn.MSELoss()  # Change loss function if needed
    optimizer = optim.AdamW(model.parameters(), lr=3e-4, weight_decay=1e-4)  # AdamW for better stability
    scheduler = optim.lr_scheduler.ReduceLROnPlateau(optimizer, mode='min', factor=0.5, patience=5)


    best_val_loss = float("inf")
    patience = 15
    patience_counter = 0
    accumulation_steps = 2

    train_losses = []
    val_losses = []

    for epoch in range(epochs):
        model.train()
        total_train_loss = 0
        optimizer.zero_grad()

        print(f"\nEpoch {epoch+1}/{epochs}")

        # Single Training Progress Bar
        with tqdm(total=len(train_loader), desc="Training", unit="batch", leave=True, dynamic_ncols=True) as train_progress:
            for i, (batch, targets, _) in enumerate(train_loader):
                batch, targets = batch.to(device), targets.to(device)
                output = model(batch)
                #loss = criterion(output, targets) / accumulation_steps
                loss = criterion(output, targets)  # Compute loss first
                loss = loss / accumulation_steps  # Normalize loss before accumulating gradients
                loss.backward()

                if (i + 1) % accumulation_steps == 0:
                    torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
                    optimizer.step()
                    optimizer.zero_grad()

                total_train_loss += loss.item() * accumulation_steps
                avg_train_loss = total_train_loss / (i + 1)

                # Update Progress Bar
                train_progress.set_postfix(train_loss=f"{avg_train_loss:.4f}")
                train_progress.update(1)

        avg_train_loss = total_train_loss / len(train_loader)
        train_losses.append(avg_train_loss)

        # Validation Loop
        model.eval()
        total_val_loss = 0

        with tqdm(total=len(eval_loader), desc="Validating", unit="batch", leave=True, dynamic_ncols=True) as val_progress:
            with torch.no_grad():
                for i, (batch, targets, _) in enumerate(eval_loader):
                    batch, targets = batch.to(device), targets.to(device)
                    output = model(batch)
                    loss = criterion(output, targets).item()
                    total_val_loss += loss

                    avg_val_loss = total_val_loss / (i + 1)

                    # Update Progress Bar
                    val_progress.set_postfix(val_loss=f"{avg_val_loss:.4f}")
                    val_progress.update(1)

        avg_val_loss = total_val_loss / len(eval_loader)
        val_losses.append(avg_val_loss)

        print(f"Epoch {epoch+1} | Train Loss: {avg_train_loss:.6f} | Validation Loss: {avg_val_loss:.6f}")

        # Learning rate adjustment
        scheduler.step(avg_val_loss)
        print(f"Current Learning Rate: {optimizer.param_groups[0]['lr']}")

        # Save best model
        if avg_val_loss < best_val_loss:
            best_val_loss = avg_val_loss
            patience_counter = 0
            model_path = os.path.join(model_dir, "best_model.pth")
            torch.save(model.state_dict(), model_path)
            print(f"Model improved! Saved to {model_path}")
        else:
            patience_counter += 1

        # Early stopping
        if patience_counter >= patience:
            print(f"Early Stopping at Epoch {epoch+1}")
            break

    # Plot Loss Curves
    plot_loss_curves(train_losses, val_losses)


# Function to Plot Training & Validation Loss
def plot_loss_curves(train_losses, val_losses):
    plt.figure(figsize=(10, 5))
    plt.plot(train_losses, label="Train Loss", marker='o')
    plt.plot(val_losses, label="Validation Loss", marker='o')
    plt.xlabel("Epochs")
    plt.ylabel("Loss")
    plt.title("Training & Validation Loss Curve")
    plt.legend()
    plt.grid(True)
    plt.show()

