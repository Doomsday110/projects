class VideoDataset(Dataset):
    def __init__(self, video_folder, flow_file, max_seq_len=742, apply_augmentations=True):
        self.video_files = sorted([
            os.path.join(video_folder, f) for f in os.listdir(video_folder) if f.endswith(".mp4")
        ])
        self.max_seq_len = max_seq_len
        self.apply_augmentations = apply_augmentations

        # Load optical flow data
        with open(flow_file, "rb") as f:
            self.flow_data = pickle.load(f)

        # Define Augmentations
        self.augment = T.Compose([
            T.RandomHorizontalFlip(p=0.5),
            T.RandomRotation(degrees=15),
            T.ColorJitter(brightness=0.3, contrast=0.3, saturation=0.3, hue=0.1),
            T.GaussianBlur(kernel_size=(3, 3), sigma=(0.1, 2.0)),
            T.RandomResizedCrop(size=(64, 64), scale=(0.8, 1.0))
        ])

    def __len__(self):
        return len(self.video_files)

    def __getitem__(self, idx):
        video_path = self.video_files[idx]
        frames = self.load_video_frames(video_path)
        path = self.flow_data.get(video_path, np.zeros((self.max_seq_len, 2)))

        frames = self.pad_or_truncate(frames)
        path = self.pad_or_truncate_path(path)

        return frames, torch.tensor(path, dtype=torch.float32), video_path

    def load_video_frames(self, video_path):
        cap = cv2.VideoCapture(video_path)
        frames = []

        while cap.isOpened():
            ret, frame = cap.read()
            if not ret:
                break

            frame = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
            frame = cv2.resize(frame, (64, 64))

            # Convert to PyTorch Tensor
            frame = torch.tensor(frame, dtype=torch.float32) / 255.0  # Normalize to [0,1]
            frame = frame.permute(2, 0, 1)  # Convert to (C, H, W)

            # 🔹 Apply Augmentations (only during training)
            if self.apply_augmentations and torch.rand(1).item() < 0.8:
                frame = self.augment(frame)

            frames.append(frame)

        cap.release()

        if len(frames) == 0:
            return torch.zeros((1, 3, 64, 64))  # Return empty tensor if no frames

        return torch.stack(frames)

    def pad_or_truncate(self, frames):
        num_frames = frames.shape[0]
        if num_frames > self.max_seq_len:
            return frames[:self.max_seq_len]  # Truncate
        else:
            pad_size = self.max_seq_len - num_frames
            return torch.cat((frames, torch.zeros((pad_size, 3, 64, 64))), dim=0)  # Pad

    def pad_or_truncate_path(self, path):
        num_frames = path.shape[0]
        if num_frames > self.max_seq_len:
            return path[:self.max_seq_len]  # Truncate
        else:
            pad_size = self.max_seq_len - num_frames
            return np.vstack((path, np.zeros((pad_size, 2))))

class CNNLSTM(nn.Module):
    def __init__(self, hidden_dim=512, num_layers=2, dropout=0.3):
        super(CNNLSTM, self).__init__()

        # CNN Feature Extractor
        self.cnn = nn.Sequential(
            nn.Conv2d(3, 64, kernel_size=3, stride=1, padding=1),
            nn.BatchNorm2d(64),
            nn.ReLU(),
            nn.MaxPool2d(2, 2),

            nn.Conv2d(64, 128, kernel_size=3, stride=1, padding=1),
            nn.BatchNorm2d(128),
            nn.ReLU(),
            nn.MaxPool2d(2, 2),

            nn.Conv2d(128, 256, kernel_size=3, stride=1, padding=1),
            nn.BatchNorm2d(256),
            nn.ReLU(),
            nn.MaxPool2d(2, 2),
        )

        # Fully Connected Layer
        self.fc = nn.Linear(256 * 8 * 8, 512)  # Adjusted to avoid overfitting
        self.dropout = nn.Dropout(dropout)

        # LSTM for Temporal Features
        self.lstm = nn.LSTM(
            input_size=512, hidden_size=hidden_dim, num_layers=num_layers,
            batch_first=True, dropout=dropout, bidirectional=False
        )

        # Output Layer
        self.layer_norm = nn.LayerNorm(hidden_dim)  # Helps stabilize training
        self.classifier = nn.Linear(hidden_dim, 2)  # No activation here

    def forward(self, x):
        batch_size, seq_len, c, h, w = x.shape

        # Merge batch & sequence dimension
        x = x.view(batch_size * seq_len, c, h, w)

        # CNN Feature Extraction
        x = self.cnn(x)
        x = torch.flatten(x, start_dim=1)
        x = self.fc(x)
        x = self.dropout(x)

        # Reshape for LSTM
        x = x.view(batch_size, seq_len, -1)

        # LSTM for sequence modeling
        x, _ = self.lstm(x)
        x = self.layer_norm(x)  # Normalize LSTM output

        # Output layer
        return self.classifier(x)  # No tanh (allows more flexible outputs)

